<p align="center"><span style="font-family: arial, helvetica, sans-serif; color: #333399; font-size: large;"><b><b><b><b><span>Comunicado de Prensa <br />El comercio minorista de la Ciudad en alerta por las comisiones al dep&oacute;sito en efectivo<br /></span></b></b></b></b></span></p>
<p style="text-align: justify;"></p>
<p style="text-align: justify;"><span style="font-size: small; font-family: arial, helvetica, sans-serif;"><i>Buenos Aires 22 de mayo de 2018</i>. - La Federaci&oacute;n de Comercio e industria de la ciudad de Buenos Aires - FECOBA - expres&oacute; hoy su preocupaci&oacute;n en torno a las consecuencias de la medida que actualmente rige en la banca p&uacute;blica y privada que estipula el cobro de un arancel extra por el dep&oacute;sito de dinero f&iacute;sico.</span></p>
<p style="text-align: justify;"><span style="font-size: small; font-family: arial, helvetica, sans-serif;">&ldquo;Desde FECOBA consideramos que este nuevo recargo provoca una mayor carga econ&oacute;mica en contra del desarrollo de las pymes, quienes son las principales damnificadas tras esta resoluci&oacute;n&rdquo;, destac&oacute; Fabi&aacute;n Castillo, presidente de la entidad. &rdquo;Entendemos el intento por reducir la circulaci&oacute;n de dinero en efectivo, pero no creemos que la suma de un nuevo impuesto sea la soluci&oacute;n&rdquo;, agreg&oacute;.</span></p>
<p style="text-align: justify;"><span style="font-size: small; font-family: arial, helvetica, sans-serif;">En ese sentido, el dirigente empresario resalt&oacute; que &ldquo;junto con el Gobierno porte&ntilde;o venimos de a&ntilde;os marcados por una lucha intensa contra el comercio ilegal, y lamentablemente creemos que esta medida s&oacute;lo premia a los que eligen mantenerse al margen del circuito formal mientras que afecta a los que hacen denodados esfuerzos por operar dentro del sistema&rdquo;.</span></p>
